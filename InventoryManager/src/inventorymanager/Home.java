    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = tfEmail.getText().trim();
        char[] pass = tfPass.getPassword();
        if(email.isEmpty() || pass == null || pass.length <= 0){
            JOptionPane.showMessageDialog(null, "some fields are empty", "Woops", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!DataValidator.validateEmail(email)){
            JOptionPane.showMessageDialog(null, "invalid email format", "Woops", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            if(DatabaseManager.getInstance().LogniUser(email, String.valueOf(pass))){
                this.setVisible(false);
                //DatabaseManager.getInstance().loadProfile();
                new User().setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null, "user not found!", "Woops", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            this.setVisible(true);
            JOptionPane.showMessageDialog(null, "some error occured", "Woops", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

