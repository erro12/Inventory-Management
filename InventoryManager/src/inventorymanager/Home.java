    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        String salutation = dropdownSalutation.getSelectedItem().toString();
        String fname = tfFirstName.getText().trim();
        String lname = tfLastName.getText().trim();
        String email = tfEmailR.getText().trim();
        char[] pass = tfPassR.getPassword();
        char[] cpass = tfConfirmPassR.getPassword();
        String address = tfAddressR.getText();
        String mobile = tfMobileR.getText();
        String gender = getSelectedButtonText(btnGroupGender);
        String role = dropdownRole.getSelectedItem().toString();
        if(gender == null || gender.isEmpty())
            gender = "Male";
        if(salutation == null || salutation.isEmpty())
            salutation = "Mr.";
        if(role == null || role.isEmpty())
            role = "User";
        if(email.isEmpty() || pass == null || pass.length <= 0 || cpass == null || cpass.length <= 0 || fname.isEmpty() || lname.isEmpty() || salutation.isEmpty()){
            JOptionPane.showMessageDialog(null, "some fields are empty", "Woops", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!DataValidator.validateEmail(email)){
            JOptionPane.showMessageDialog(null, "invalid email format", "Woops", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(mobile == null || mobile.isEmpty())
            mobile = "";
        else{
            if(!DataValidator.validateMobile(mobile)){
                JOptionPane.showMessageDialog(null, "invalid mobile number format", "Woops", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        if(!Arrays.equals(pass, cpass)){
            JOptionPane.showMessageDialog(null, "Passwords do not match.", "Woops", JOptionPane.ERROR_MESSAGE);
            return;
        }
        System.out.println("full name: "+salutation+" "+fname+" "+lname);
        try {
            if(emailExists(email)){
                JOptionPane.showMessageDialog(null, "a user with entered email already exists.", "Woops", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if(DatabaseManager.getInstance().RegisterUser(fname, lname, email, String.valueOf(pass), mobile, role.equalsIgnoreCase("admin") ? 1 : 0, 1, gender, address, salutation)){
                JOptionPane.showMessageDialog(null, "registered successfully");
                switchToLogin();
            }else{
                JOptionPane.showMessageDialog(null, "registration failed, try again.", "Woops", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "some error occured", "Woops", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSignupActionPerformed

    private boolean emailExists(String email) throws SQLException{
        return DatabaseManager.getInstance().emailExists(email);
    }
    
    public String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();

            if (button.isSelected()) {
                return button.getText();
            }
        }

        return null;
    }
    
